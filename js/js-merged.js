$(document).ready(function(){

	/***********************************************
	*************  EXTERNAL LINKS  *****************
	************************************************/
	$("a[rel=external]").each(function() {
		$(this).addClass('external');
		$(this).attr({
			title: "Open this page in a new window",
			target: "_blank"
		});
	});





});


/***********************************************
**********  EQUAL HEIGHT  **********************
************************************************/

function equalHeight(group, callback) {
    tallest = 0;
    group.each(function () {
        thisHeight = $(this).height();
        if (thisHeight > tallest) {
            tallest = thisHeight;
        }
    });
    group.css('height', tallest);

    if(callback)
    	callback(group, tallest);
}

/***********************************************
********** SCROLL TO ELEMENT ******************
************************************************/
function scrollToEl(el, offset){
	var pos 	= el.offset(),
		off_y	= offset?offset:0;
		pos_y 	= pos.top - off_y;
	//console.log(el);
	$('body,html').animate({
		scrollTop:pos_y
	})
}